# A Python program that takes two strings as input and concatenates them with a space in between.

# Taking 2 string input from the user
str1 = input("Enter the first string: ")
str2 = input("Enter the second string: ")

# Concatinating the 2 strings with the use of + operator
str3 = str1 + " " + str2

# Printing the answer string
print(f"Final String Is: {str3}")